// Arrow function ㅇㅕㄴㅅㅡㅂ

const plus = (p1, p2) => {
    return p1 + p2;
};

const minus = (p1, p2) => {
    return p1 - p2;
};

const multi = (p1, p2) => {
    return p1 * p2;
};

const div = (p1, p2) => {
    return p1 / p2;
};

console.log(plus(1,2));
console.log(minus(1,2));
console.log(multi(1,2));
console.log(div(1,2));

